      <h4><i class="fas fa-question-circle"></i>What is physical therapy?</h4>
      </div></td>

      <td>
        <p class="par">Physical therapy (PT) helps people recover from surgery, an accident or injury. PT also helps when a person’s body is not functioning at what is considered normal for them—perhaps they had a heart attack, stroke or fall; or are dealing with a neurological disease; or even facing aging and incontinence. Physical therapy helps when the body is changing in a way that is affecting function and quality of life. A physical therapist evaluates a person from the standpoint of their mobility, balance and strength. What are they having difficulty doing? What do they want to get back to doing? The basic premise of physical therapy is that you’re looking at the person’s physical dysfunction and determining the steps to take to improve the person’s physical condition.</p>
      </td>
    </tr>

    <tr>
      <td><div class="con">
      <h4><i class="fas fa-question-circle"></i>What happens in a therapy session?</h4>
      </div></td>

      <td>
        <p class="par">There’s always an element of patient education to help people discover what they are doing on a daily basis that could be contributing to their problem. The physical therapist may need to re-educate a person on lifting something correctly or moving properly. During a PT session, the therapist usually applies hands-on techniques such as joint mobilization for a stiff joint, or soft-tissue work of stretching or massaging tight muscles. Some physical therapists also do manual manipulation, which uses careful, directed manual force to improve mobility in joints, connective tissue or skeletal muscles.

PT always involves exercises of some sort, from a low-level stretch all the way to a well-rounded strengthening program. Physical therapy modalities are rehabilitation technologies that relieve pain, reduce swelling, decrease muscle spasms and improve circulation. These modalities include ultrasound, trigger-point pressure, dry needling, soft-tissue oscillation, electrical stimulation, traction, biofeedback, heat and ice. The exercise prescription and hands-on techniques are central to each PT session.</p>
      </td>
    </tr>

