        <div class="con">
        <h4><i class="fas fa-question-circle"></i>Why do I have to do these exercises?</h4>
        </div>
      </td>

      <td>
        <p class="par">Physical therapists sometimes push people beyond their limit or comfort zone a little bit, encouraging them to go beyond what they think they can do. Physical therapists try to look at things objectively and implement different tests with patients. We try to empower people to kind of fix themselves. We don’t want them to have to keep coming back. Ultimately, we’re trying to get people to a point where they can maintain themselves and do their exercises at home or at the gym and go on with their lives. We push people so they can be independent and not reliant on us, medication or other treatments.</p>
      </td>
    </tr>

