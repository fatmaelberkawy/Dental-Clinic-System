      <td>
        <div class="con"><h4><i class="fas fa-question-circle"></i>How does physical therapy differ for older adults?</h4></div>
      </td>

      <td>
        <p class="par">The timeline of healing and recovery is going to be longer, because with the aging process, seniors don’t heal as quickly as someone younger. But at the same time, I still think seniors are not a population of people who should be discounted. I don’t want people to say, “You’re 90, who cares?” You look at the individual, what their life is like, how active they are and their motivation. You may not be as aggressive with some of the modalities and exercises for seniors, but you continue to treat them like you would anyone else. You just expect their physical recovery to take longer.</p>
      </td>
    </tr>

    <tr>
      <td>
        <div class="con"><h4><i class="fas fa-question-circle"></i>What’s the difference between physical therapy and occupational therapy (OT)?</h4></div>
      </td>

      <td>
        <p class="par">Physical therapy deals with the entire body, anywhere from the inner ear and vision to the arms and legs. Physical therapists look at the body as a whole and are focused on getting people up and walking and working on balance, overall strength and core strength. Occupational therapy specializes a little more in the upper extremities, arms and hands. OT is very task-related and goes more into depth with someone’s fine motor skills and memory-type exercises and cognitive strategies. Often PTs and OTs collaborate with others on a therapy team—doctors, nurses, social workers, speech therapists, recreational therapists and music therapists.</p>
      </td>
    </tr>

