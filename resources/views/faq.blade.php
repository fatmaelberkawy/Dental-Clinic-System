        <div class="con">
        <h4><i class="fas fa-question-circle"></i>Why does physical therapy take longer than I had thought?</h4>
        </div>
      </td>

      <td>
        <p class="par">Physical therapy is not a quick fix. Americans in general are notorious for looking for that quick fix. PT follows the way our anatomy and bodies operate. There’s a certain amount of time that is required for certain types of tissue in the body to repair themselves. If you try to speed up the process, you could experience a re-injury. So physical therapy takes some patience and perseverance, and you’ve got to keep up with the program to experience the gains. Physical recovery happens in stages, and you have to be present for the whole program, from beginning to end, if you want lasting results.</p>
      </td>
    </tr>

